    private void btnContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContinuarActionPerformed
        String usuario = this.txtUsuario.getText();
        String contrasena = this.txtContrasena.getText();
        if(!usuario.equals("") && !contrasena.equals("")){
            conectar cc = new conectar();
            Connection cn = cc.conexion();
            String sql = "";
            sql = "SELECT * FROM usuarios WHERE (usuario='" + usuario + "'  AND contrasena='" + contrasena + "')";
            String[] datos = new String[3];
            try {
                Statement st = cn.createStatement();
                ResultSet rs = st.executeQuery(sql);
                while (rs.next()) {
                    datos[0] = rs.getString(1);
                    datos[1] = rs.getString(2);
                    datos[2] = rs.getString(3);
                }
                if(datos[2] != null){
                    if(datos[2].equals("Administrador")){
                        this.setVA(new ventanaAdministrador_1());
                        this.getVA().setVisible(true);
                        dispose();
                    }else{
                        if(datos[2].equals("Vendedor")){
                            this.setVV(new ventanaVendedor_1());
                            this.getVV().setVisible(true);
                            dispose();
                        }else{
                            JOptionPane.showMessageDialog(null, "Imposible ingresar");
                        }
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "No se encontro o no esta escrito correctamente");
                }
            }catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Error");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Ingrese una contrasena");
        }
        

    
    }//GEN-LAST:event_btnContinuarActionPerformed

